# If you want double quotes in your string,
# it is possible:

message_1 = 'Python "programming'
print(message_1)

# Here we use double quotes to contain our value
# we then use an escape character to add a double quote in our string:

message_2 = "Python \"programming"
print(message_2)

# Other possible escape characters in Python are:

#   Sequence    |       Meaning        |   ASCII/ISO code

#       \\              Backslash               0x5c
#       \'              Single quote            0x27
#       \"              Double quote            0x22
#       \a              Bell                    0x07
#       \b              Backspace               0x08
#       \f              Form feed               0x0c
#       \n              Newline                 0x0a
#       \r              Carriage return         0x0d
#       \t              Tab                     0x09
#       \v              Vertical tab            0x0b
