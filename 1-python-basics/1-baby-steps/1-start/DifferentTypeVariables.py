# Integer:

students_count = 1000
print(students_count)

# Floating number:

rating = 4.99
print(rating)

# Boolean:

is_published = False
print(is_published)

# String:

string_sq = "This is a literal string"
print(string_sq)

string_dq = 'This is another string'
print(string_dq)

multiple_line_string = """
Multiple
line
String
"""
print(multiple_line_string)


# Different ways of initialising variables:

# First way:

x = 1
y = 2

print(x, y)

# Second way:

z, h = 1, 2
print(z, h)

# Third way:

first_var = second_var = 1  # Why Python why?
print(first_var, second_var)

